public class cred {
    String name;
    int age;
    String major;
    long num;
    long idNum;

    cred(String name, String major, long num, int age, long idNum) {
        this.name = name;
        this.major = major;
        this.num = num;
        this.age = age;
        this.idNum = idNum;
    }
}