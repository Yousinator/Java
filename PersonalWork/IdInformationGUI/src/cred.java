public class cred {
    String name;
    int age;
    String major;
    int uniNum;
    int idNum;

    cred(String name, String major, int uniNum, int age, int idNum) {
        this.name = name;
        this.major = major;
        this.uniNum = uniNum;
        this.age = age;
        this.idNum = idNum;
    }
}